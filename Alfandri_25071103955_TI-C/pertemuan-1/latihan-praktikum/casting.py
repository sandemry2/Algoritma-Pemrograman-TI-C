#Specify_a_Variable_Type

#integer
x = int(1)   # x akan bernilai 1
y = int(2.8) # y akan bernilai 2
z = int("3") # z akan bernilai 3

#float
x = float(1)     # x akan bernilai 1.0
y = float(2.8)   # y akan bernilai 2.8
z = float("3")   # z akan bernilai 3.0

#string
x = str("s1")
y = str(2)    # y akan berubah menjadi '2'
z = str(3.0)  # z akan berubah menjadi '3.0'