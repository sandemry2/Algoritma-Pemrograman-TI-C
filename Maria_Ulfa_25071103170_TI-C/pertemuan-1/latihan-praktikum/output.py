##print text
#untuk mencetak teks harus menggunakan tanda kutip
print("hari ini menyenangkan")
print('hari ini menyenangkan')

#untuk mencetak tanpa baris baru, gunakan parameter: end
print("hari ini menyenangkan", end=" ")
print("dan seru")


##print numbers
#untuk mencetak angka gunakan fungsi: print() tanpa tanda kutip
print(3)
print(2345)

#operasi matematika dapat digunakan dalam fungsi: print()
print(3 + 5)
print(5 * 4)

#untuk mencampurkan teks dan angka dalam satu output, pisahkan dengan koma
print("saya berumur", 19, "tahun")